        private record Slope(int Right, int Down);

        public static int Run1()
        {
            bool[][] forest = ParseInput<string>(Input).Select(ParseRow).ToArray();
            int trees = CountTrees(forest, new Slope(3,1 ));

            return trees;
        }

        public static long Run2()
        {
            bool[][] forest = ParseInput<string>(Input).Select(ParseRow).ToArray();
            return 
                (long)CountTrees(forest, new Slope(1,1 )) * 
                CountTrees(forest, new Slope(3,1 )) *
                CountTrees(forest, new Slope(5,1 )) *
                CountTrees(forest, new Slope(7,1 )) *
                CountTrees(forest, new Slope(1,2 ));
        }

        private static int CountTrees(bool[][] forest, Slope slope)
        {
            bool Peek(int row, int col)
            {
                bool[] r = forest[row];
                return r[col % r.Length];
            }

            int x = 0, y = 0, trees = 0;
            while (y < forest.Length)
            {
                if (Peek(y, x))
                {
                    trees++;
                }
                x += slope.Right;
                y += slope.Down;
            }

            return trees;
        }

        private static bool[] ParseRow(string row)
        {
            return row.Trim().Select(x => x == '#').ToArray();
        }

